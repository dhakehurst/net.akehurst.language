internal class test_abcz_OR_abc : test_ScanOnDemandParserAbstract() {

    // S =  ABCZ | ABC
    // ABCZ = a b c z
    // ABC = a b c

    private companion object {
        val rrs = runtimeRuleSet {
            choice("S", RuntimeRuleChoiceKind.LONGEST_PRIORITY) {
                ref("ABCZ")
                ref("ABC")
            }
            concatenation("ABCZ") { literal("a"); literal("b"); literal("c"); literal("z") }
            concatenation("ABC") { literal("a"); literal("b"); literal("c")}
        }
        val goal = "S"
    }

    @Test
    fun empty_fails() {
        val sentence = ""

        val (sppt, issues) = super.testFail(rrs, goal, sentence, expectedNumGSSHeads = 1)
        assertNull(sppt)
        assertEquals(listOf(
            parseError(InputLocation(0,1,1,1),"^",setOf("'a'"))
        ),issues)
    }

    @Test
    fun a_fails() {
        val sentence = "a"

        val (sppt, issues) = super.testFail(rrs, goal, sentence, expectedNumGSSHeads = 1)
        assertNull(sppt)
        assertEquals(listOf(
            parseError(InputLocation(1,2,1,1),"a^",setOf("'b'"))
        ),issues)
    }

    @Test
    fun abc() {
        val sentence = "abc"

        val expected = """
         S|1 { ABC { 'a' 'b' 'c' } }
        """.trimIndent()

        super.test(
            rrs = rrs,
            goal = goal,
            sentence = sentence,
            expectedNumGSSHeads = 1,
            expectedTrees = arrayOf(expected)
        )
    }


}