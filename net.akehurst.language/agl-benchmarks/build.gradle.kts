// =====================================================================
// JMH benchmark wiring (option B from the planning step: no plugin).
//
// The pipeline is:
//   compileTestKotlinJvm     (existing) — compiles ParserBenchmarks.kt
//      → jmhGenerate         — runs JmhBytecodeGenerator on those classes
//                              to emit benchmark stub Java sources +
//                              META-INF/BenchmarkList resource
//      → jmhCompileGenerated — javac the generated stubs
//      → jmh                 — runs org.openjdk.jmh.Main with the combined
//                              classpath (test classes + generated stubs)
//
// Usage:
//   ./gradlew :agl-parser:jmh
//   ./gradlew :agl-parser:jmh -Pjmh.include='.*Multi.*'
//   ./gradlew :agl-parser:jmh -Pjmh.args='-f 5 -wi 10 -i 20'
// =====================================================================

val jvmTestCompilation = kotlin.jvm().compilations.getByName("test")

val jmhGeneratedSrcDir = layout.buildDirectory.dir("jmh/generated-src")
val jmhGeneratedResDir = layout.buildDirectory.dir("jmh/generated-resources")
val jmhGeneratedClsDir = layout.buildDirectory.dir("jmh/generated-classes")
// Results are checked-in-able artefacts: live in the module source tree
// (agl-parser/results/) rather than under build/, so that successive runs
// can be diffed across commits.
val jmhResultsDir      = layout.projectDirectory.dir("results")

val jmhGenerate by tasks.registering(JavaExec::class) {
    group = "benchmark"
    description = "Generate JMH benchmark stub Java sources from compiled @Benchmark classes."
    dependsOn("jvmTestClasses")
    mainClass.set("org.openjdk.jmh.generators.bytecode.JmhBytecodeGenerator")

    val testOutputs = jvmTestCompilation.output.allOutputs
    val testRuntimeCp = jvmTestCompilation.runtimeDependencyFiles
    classpath = files(testOutputs) + testRuntimeCp

    val classesDirsProvider = files(jvmTestCompilation.output.classesDirs)
    val srcOutDir = jmhGeneratedSrcDir
    val resOutDir = jmhGeneratedResDir

    inputs.files(testOutputs)
    outputs.dir(srcOutDir)
    outputs.dir(resOutDir)

    doFirst {
        val srcOut = srcOutDir.get().asFile.also { it.mkdirs() }
        val resOut = resOutDir.get().asFile.also { it.mkdirs() }
        // JmhBytecodeGenerator only takes ONE compiled-bytecode input dir;
        // pick the kotlin/jvm/test classes dir that actually contains our
        // benchmark package.
        val benchClassesRoot = classesDirsProvider.files.firstOrNull {
            it.resolve("net/akehurst/language/agl/benchmark").exists()
        } ?: error(
            "Could not locate compiled benchmark classes in ${classesDirsProvider.files}.\n" +
                    "Did compileTestKotlinJvm produce classes for package net.akehurst.language.agl.benchmark?"
        )
        // Args: <compiled-classes> <out-src> <out-resources> <generator: default|asm|reflection>
        args = listOf(
            benchClassesRoot.absolutePath,
            srcOut.absolutePath,
            resOut.absolutePath,
            "default"
        )
    }
}

val jmhCompileGenerated by tasks.registering(JavaCompile::class) {
    group = "benchmark"
    description = "Compile JMH-generated stub Java sources."
    dependsOn(jmhGenerate)
    source(jmhGeneratedSrcDir)
    include("**/*.java")
    classpath = files(jvmTestCompilation.output.allOutputs) + jvmTestCompilation.runtimeDependencyFiles
    destinationDirectory.set(jmhGeneratedClsDir)
    sourceCompatibility = "11"
    targetCompatibility = "11"
    options.isFork = false
    options.encoding = "UTF-8"
}

tasks.register<JavaExec>("jmh") {
    group = "benchmark"
    description = "Run JMH benchmarks. Filter with -Pjmh.include=<regex>, extra args via -Pjmh.args='...'."
    dependsOn(jmhCompileGenerated)
    mainClass.set("org.openjdk.jmh.Main")

    classpath = files(
        jmhGeneratedClsDir,
        jmhGeneratedResDir,
        jvmTestCompilation.output.allOutputs
    ) + jvmTestCompilation.runtimeDependencyFiles

    // Sensible JVM defaults for steady-state JIT measurement.
    jvmArgs = listOf(
        "-Xms2g", "-Xmx2g",
        "-XX:+AlwaysPreTouch",
        "-XX:+UseG1GC"
    )

    val include = providers.gradleProperty("jmh.include").orElse(".*")
    val extra   = providers.gradleProperty("jmh.args").orElse("")
    // Each run lands in its own timestamped pair of files so that historical
    // runs can be diffed. Override with -Pjmh.resultsName=<basename> to pin
    // a name (useful for "before" / "after" comparisons of a single change).
    val resultsBasename = providers.gradleProperty("jmh.resultsName").orElse(
        DateTimeFormatter
            .ofPattern("yyyyMMdd-HHmmss")
            .withZone(ZoneId.systemDefault())
            .format(Instant.now())
    )
    val resultsDir = jmhResultsDir

    doFirst { resultsDir.asFile.mkdirs() }

    argumentProviders.add(CommandLineArgumentProvider {
        val base = resultsBasename.get()
        // Result format: controlled by -Pjmh.rf (text|csv|scsv|json|latex).
        // Default is "text" (space-padded table). Use "csv" or "scsv" for
        // a separator-friendly file that opens cleanly in spreadsheet tools.
        val rf = providers.gradleProperty("jmh.rf").orElse("scsv").get()
        val ext = when (rf) { "csv" -> "csv"; "scsv" -> "scsv"; "json" -> "json"; "latex" -> "tex"; else -> "txt" }
        val outFile = resultsDir.file("jmh-$base.$ext").asFile.absolutePath
        buildList {
            add(include.get())
            add("-rf"); add(rf)
            add("-rff"); add(outFile)
            // Extra user args last so they can override anything above —
            // e.g. -Pjmh.args='-rf json -rff results/jmh.json' to also
            // emit JSON for scripted comparison.
            extra.get().takeIf { it.isNotBlank() }?.let { addAll(it.split(Regex("\\s+"))) }
        }
    })

    doLast {
        val base = resultsBasename.get()
        val rf = providers.gradleProperty("jmh.rf").orElse("scsv").get()
        val ext = when (rf) { "csv" -> "csv"; "scsv" -> "scsv"; "json" -> "json"; "latex" -> "tex"; else -> "txt" }
        logger.lifecycle("JMH results written to: ${resultsDir.file("jmh-$base.$ext").asFile}")
    }
}
